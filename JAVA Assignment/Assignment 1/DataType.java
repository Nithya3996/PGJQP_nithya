class DataType
{
public static void main(String[] args)
{
int num; 
float f; 
char ch; 
String str;
num=10;
f=1.12f;
ch='N';
str="I am Nithya";
System.out.println("Value of num: "+num);
System.out.println("Value of f: "+f);
System.out.println("Value of ch: "+ch);
System.out.println("Value of str: "+str);
}
}
